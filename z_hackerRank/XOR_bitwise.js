//Crie uma função que receba dois números inteiros e retorne o resultado de a ^ b.

function xorBasico(a, b) {

    const calc = a ^ b; // ^ = faz o XOR bit a bit
    return calc

}

console.log(xorBasico(5, 3)) // retorna 6 pois 5 = 101 e 3 = 011 

